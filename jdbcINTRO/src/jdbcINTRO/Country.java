package jdbcINTRO;

public class Country {
	private String id;
	private String ad;
	private String soyad;
	private String derse_girme;
	private String durum;
	
	public Country(String id, String ad, String soyad, String derse_girme, String durum) {
		this.id = id;
		this.ad = ad;
		this.soyad = soyad;
		this.derse_girme = derse_girme;
		this.durum = durum;
	}
	
}
